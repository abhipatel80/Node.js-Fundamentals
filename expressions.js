// ------------------------------------Variable Expressions------------------------------------
let x = 10;
const y = 20;

// ------------------------------------Arithmetic Expressions------------------------------------
const sum = x + y;
const multiply = x * y;

// ------------------------------------Comparison Expressions------------------------------------
const equal = x === y;
const greater = x > y;

// ------------------------------------Logical Expressions------------------------------------
const and = x && y;
const or = x || y;

// ------------------------------------Function Expressions------------------------------------
const add = function (a, b) {
    return a + b;
};

// ------------------------------------Object Expressions------------------------------------
const person = { firstName: 'Abhi', lastName: 'Patel' };

// ------------------------------------Array Expressions------------------------------------
const numbers = [1, 2, 3, 4, 5];

// --------------------------------Conditional (Ternary) Expressions--------------------------------
const isEven = x % 2 === 0 ? 'Even' : 'Odd';

